  targetBox.textContent = `Target: ${dailyTarget}`;

  // Update best score today
  if (dailyBest === null) {
    dailyBestScoreDisplay.textContent = 'N/A';
  } else {
    dailyBestScoreDisplay.textContent = dailyBest;
  }

